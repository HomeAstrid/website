<h2>Home Astrid: een terugblik</h2>
    <p><br>
      Op 28 oktober 1966 wordt Home Astrid plechtig ingehuldigd, en naar aanleiding  daarvan schrijft De Gentenaar het volgende.&nbsp;<br>
  &quot;Bij een bezoek aan de totnogtoe ingerichte kamers, dringt zich telkens  weer de vergelijking op met een eersterangshotel. 
  Althans en in de eerste  plaats wat betreft het uitgebreid comfort waarover al deze kamers beschikken.  De ramen met hun draaiende vensters die een goede verluchting toelaten, zijn  bovendien voorzien van zonneblinden. 
  De spiegel boven het wasbekken is,  bijzonder handig voor de damesopschik, in drieluik opgevat met daarachter een  toiletkastje. Vanzelfsprekend is er een makkelijk eenpersoonsbed en een  ingebouwde kleerkast.&quot;<br>
  <br>
      Een andere publicatie vat Home Astrid als volgt samen.&nbsp;<br>
  &quot;De laatste technische snufjes zijn bij het optrekken van dit  meisjestehuis aangewend en zelfs derwijze dat een luxueus hotel in een of  andere villegiatuurstad,
  er in de leer zou kunnen gaan. Men betreedt vooreerst  een ruime, uitstekend verlichte hall met links de kantoren voor de econoom en  in het midden, naast de trap, de receptie en de telefooncentrale.
  Op de woning  van de huisbewaarder na, is dan de ganse verdere benedenverdieping voorbehouden  aan een eetgelegenheid of &quot;cafetaria&quot; en een recreatieplas.
  Beide  plaatsen vloeien in elkaar over met in het midden dan een binnenhuisfontein  wier watergeklater en aangepaste verlichting, een zeer bijzondere sfeer  schept.&quot;<br>
  <br>
      De Homeraad wordt in 1971-1972 opgericht door Johan De Cock.</p>
